#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n;
    cin>>n;
    map<ll,ll> m;
    int x;
    for(int i=0;i<n;i+=1){
        cin>>x;
        m[x]+=1;
    }
    ll ans=0,curr=0,last=-1;
    for(auto &[i,j]:m){
        if(i==last+1){
            if(j>=curr)
                curr=j;
            else{
                ans+=(curr-j);
                curr=j;
            }
            last=i;
        }
        else{
            ans+=curr;
            curr=j;
            last=i;
        }
    }
    ans+=curr;
    cout<<ans;nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}