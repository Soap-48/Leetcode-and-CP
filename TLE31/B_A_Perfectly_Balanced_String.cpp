#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    string s;
    cin>>s;
    set<char> ss(all(s));
    ll f=ss.size();
    vi a(26,0);
    for(int i=0;i<f;i+=1){
        a[s[i]-'a']+=1;
        if(a[s[i]-'a']>=2){
            cout<<"NO\n";
            return;
        }
    }
    for(int i=f;i<s.size();i+=1){
        if(s[i]!=s[i-f]){
            cout<<"NO\n";
            return;
        }
    }
    cout<<"YES\n";
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}