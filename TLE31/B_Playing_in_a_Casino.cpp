#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n,m;
    cin>>n>>m;
    vector<vector<ll>> a(m,vll(n));
    for(int i=0;i<n;i+=1)
        for(int j=0;j<m;j+=1)
            cin>>a[j][i];
    for(int i=0;i<m;i+=1)
        sort(rall(a[i]));
    ll ans=0;
    for(int i=0;i<m;i+=1){
        for(int j=0;j<n;j+=1){
            ans+=(a[i][j]*(n+1-(2*(j+1))));
        }
    }
    cout<<ans;nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}