#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n,x;
    cin>>n>>x;
    vll a(n);
    for(int i=0;i<n;i+=1)
        cin>>a[i];
    sort(all(a));
    vll p(n+1,0);
    for(int i=0;i<n;i+=1)
        p[i+1]=p[i]+a[i];
    ll ans=0;
    for(ll i=1;i<=n;i+=1){
        if(x>=p[i]){
            ans+=1;
            ans+=((x-p[i])/i);
        }
        else
            break;
    }
    cout<<ans;
    nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}