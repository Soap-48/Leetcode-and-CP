#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n,c;
    cin>>n>>c;
    vll a(n);
    ll o=0,e=0;
    for(int i=0;i<n;i+=1){
        cin>>a[i];
        if(a[i]&1)  o+=1;
        else e+=1;
    }
    ll ans=(((c+1)*(c+2))/2);
    for(int i=0;i<n;i+=1){
        ans-=(1+(a[i]/2));
        ans-=(1+c-a[i]);
    }
    ans+=((o*(o+1))/2)+((e*(e+1))/2);
    cout<<ans;
    nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}