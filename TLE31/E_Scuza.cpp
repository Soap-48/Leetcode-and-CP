#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n,q;
    cin>>n>>q;
    vll a(n+1),p(n+1);
    a[0]=0;
    p[0]=0;
    for(int i=1;i<=n;i+=1){
        cin>>a[i];
        p[i]=p[i-1]+a[i];
        a[i]=max(a[i],a[i-1]);
    }
    ll k;
    for(int i=0;i<q;i+=1){
        cin>>k;
        int ind=upper_bound(all(a),k)-a.begin()-1;
        if(ind<0) ind=0;
        cout<<p[ind]<<' ';
    }
    nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}