#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n;
    cin>>n;
    vll a(n),b(n);
    for(int i=0;i<n;i+=1){
        cin>>a[i]>>b[i];
    }
    ll l=0,r=n+1;
    while(r-l>1){
        ll m=(r+l)/2;
        ll x=0;
        for(int i=0;i<n;i+=1){
            if(a[i]>=m-x-1&&b[i]>=x)
                x+=1;
        }
        if(x>=m){
            l=m;
        }
        else
            r=m;
    }
    cout<<l;nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}