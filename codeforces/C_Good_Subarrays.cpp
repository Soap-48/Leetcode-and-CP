#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n;
    cin>>n;
    string s;
    cin>>s;
    map<ll,ll> m;
    m[0]=1;
    ll ans=0;
    ll curr=0;
    for(int i=0;i<n;i+=1){
        curr+=(s[i]-'0');
        curr-=1;
        m[curr]+=1;
    }
    for(auto &[i,j]:m)
        ans+=(j*(j-1))/2;
    cout<<ans;
    nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}