#include <bits/stdc++.h>

using ll=long long;
#define vi vector<int>
#define vll vector<ll>
#define all(v) (v).begin(),(v).end()
#define rall(v) (v).rbegin(),(v).rend()
#define nl cout<<'\n'
const int MOD = 1000000007;
using namespace std;

void solve(){
    ll n;
    cin>>n;
    map<ll,ll> m;
    ll x;
    set<ll> s;
    for(ll i=0;i<((n*(n-1))/2);i+=1){
        cin>>x;
        s.insert(x);
        m[x]+=1;
    }
    ll f=n-1;
    for(auto &i:s){
        while(m[i]){
            cout<<i<<' ';
            m[i]-=f;
            f-=1;
        }
    }
    cout<<"1000000000";
    nl;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}